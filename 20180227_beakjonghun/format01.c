int main(void) {
	printf("%d\n", 1234); //1234
	printf("%d, %d\n", 1234, -5678);//1234  , -5678  음수나 양수가 아닌 정렬을 말함.
	printf("%+d, %+d\n", 1234, -5678);//+1234, -5678

	printf("%8d\n", 1234);
	printf("%08d\n", 1234);
	printf("%-08d\n", 1234);
	/*
	result 
	1234
	1234, -5678
	+1234, -5678
	1234
	00001234
	1234
	*/
	return 0;
}